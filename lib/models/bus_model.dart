class Bus {
  final String? destination;
  final String? from;
  final String? year;
  final String? month;
  final String? date;
  final String? departureTime;
  final String? busName;
  final String? route;

  Bus({
    required this.destination,
    required this.from,
    required this.year,
    required this.month,
    required this.date,
    required this.departureTime,
    required this.busName,
    required this.route,
  });
}
